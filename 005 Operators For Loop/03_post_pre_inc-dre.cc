#include<iostream>
using namespace std;
int main() {
    int i = 1;
    cout<<(i++)<<endl;
    cout<<i<<endl;

    int j = 5;
    cout<<(++j)<<endl;
    cout<<j<<endl;

    int k = 8;
    cout<<(k--)<<endl;
    cout<<k<<endl;

    int l = 5;
    cout<<(--l)<<endl;
    cout<<l<<endl;
     
    return 0;
}